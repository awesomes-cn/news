<script>
  import axios from '~plugins/axios'
  import Cookie from 'js-cookie'
  export default {
    data () {
      return {
        uid: '',
        pwd: '',
        isHideMenu: true,
        showmemeus: false,
        searchKey: ''
      }
    },
    watch: {
      '$route': function () {
        this.searchKey = this.$route.query.q
      }
    },
    computed: {
      session () {
        return this.$store.state.session
      }
    },
    methods: {
      // 登录
      login: function () {
        let self = this
        axios().post(`session/login`, { uid: this.uid, pwd: this.pwd }).then(res => {
          if (!res.data.status) {
            this.$alert('danger', '登录失败，用户名或密码错误')
            Cookie.set('awlogin', null)
          } else {
            this.$alert('success', '登录成功！')
            Cookie.set('awlogin', res.data.token)
            self.hideLogin()
            this.$store.commit('setUser', res.data.mem)
          }
        })
      },
      // 注销
      logout: function () {
        Cookie.set('awlogin', null)
        this.$store.commit('setUser', null)
        this.$alert('success', '注销成功！')
      },
      // 搜索
      searchGo: function () {
        if (this.searchKey.trim() === '') {
          return
        }
        this.$router.push({path: '/search', query: {q: this.searchKey}})
      }
    }
  }
</script>

